public class HistoryCar {
    private String id;
    private String carPostion;
    private String carRemark;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCarPostion() {
        return carPostion;
    }

    public void setCarPostion(String carPostion) {
        this.carPostion = carPostion;
    }

    public String getCarRemark() {
        return carRemark;
    }

    public void setCarRemark(String carRemark) {
        this.carRemark = carRemark;
    }
}
